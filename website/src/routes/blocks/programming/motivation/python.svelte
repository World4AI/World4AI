<p>At the moment of writing Python has completely taken over the machine learning world. In most cases you do not have good alternatives to the libraries that have been written in Python or that use Python as the main language as the interface. NumPy, TensorFlow, PyTorch, JAX, Gym and many more have become the de facto standards in the industry. This seeming lack of alternatives makes things easier for us. Python is an extremely beginner friendly programming language. You do not have to take care of memory allocation and the abundance of tutorials makes the process of learning enjoyable. But Python can be used for much more than just machine learning. Backend web development, finance and task automation come to mind for example. Should you decide down the road that AI is not for you, the lessons that you learned along the way will still be valuable.</p>
