<h1>Linear Regression</h1>
<Question
  >What is the relationship between neural networks and linear regression?</Question
>
<div class="separator" />

<p>
  Linear regression is hardly used in practice for problems with huge datasets
  and complex problems. So what is the purpose of studying linear regression
  when neural networks produce much better results? Linear regression is a
  precursor to neural networks. Linear regression models are essentially the
  simplest neurons we can think of, therefore it is convenient to study the
  basic concepts that are used in all neural networks by studying linear
  regression. When we finally arrive at more complex deep neural networks, we
  will be familiar with the basic building blocks of each neuron.
</p>
