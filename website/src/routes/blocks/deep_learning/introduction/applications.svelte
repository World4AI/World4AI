<h2>Computer Vision</h2>
<p>
  The first groundbreaking successes in deep learning were achieved in computer
  vision in the ImageNet competition. Since then we have utilized deep learning
  for image classification (ability to classify images into different categories
  like cats or dogs), object detection (the ability to find and draw a bounding
  box around specific objects) and neural style transfer (the ability to change
  the style of an image).
</p>
<div class="separator" />

<h2>Natural Language Processing</h2>
<p>
  For the longest time we were not especially successful when it came to using
  deep learning for natural languages processing (nlp). This changed drastically
  once the Transformer architecture and its derivatives was invented by Google.
  Since then we have drastically imporoved in translation tasks, text
  classification tasks and text generation tasks.
</p>
